import Head from "next/head";
import ResultsTemplate from "@/components/ResultsTemplate";
import { RecordContextProvider } from "@/context/RecordContext";
import { useRouter } from "next/router";
import mainMagician from "@/public/1347 1 (1).png"
import mainPirate from "@/public/818.png"
import mainArcher from "@/public/1337.png"
import mainWarrior from "@/public/1160.png"

export default function Result() {

  const router = useRouter()
  
  const titleValue = (res) => {
    switch(res) {
        case "magician" :
            return "MAGICIAN"
        break
        case "pirate" :
            return "PIRATE"
        break
        case "archer" :
            return "ARCHER"
        break
        case "warrior" :
            return "WARRIOR"
        break
    }
}

    const mainImgSrcValue = (res) => {
      switch(res) {
          case "magician" :
              return mainMagician
          break
          case "pirate" :
              return mainPirate
          break
          case "archer" :
              return mainArcher
          break
          case "warrior" :
              return mainWarrior
          break
      }
    }

    const characteristicsValue = (res) => {
      switch(res) {
          case "magician" :
              return "#creative #resilient #passionate"
          break
          case "pirate" :
              return "#adaptable #thrives-under-pressure"
          break
          case "archer" :
              return "#patient #high-standards #persistence"
          break
          case "warrior" :
              return "#driven #diligent #detail-oriented"
          break
      }
  }

  const descriptionValue = (res) => {
    switch(res) {
        case "magician" :
            return (<p>
                <span style={{paddingBottom: "7px", display: "block"}}>You are a dreamer.</span>

                You may have grand ideas, but can <br />
                lack the focus and motivation to <br />
                execute your plans...
            </p>)
        break
        case "pirate" :
            return (<div>
                  You are a crisis-maker. <br />

                  You may have the tendency to thrive <br />
                  on the adrenaline rush of doing things <br />
                  last minute...
          </div>)
        break
        case "archer" :
            return (<div>
                You are a perfectionist. <br />

                You may set unrealistic standards, as <br />
                you have the fear of failure and/or <br />
                criticism...
            </div>)
        break
        case "warrior" :
            return (<div>
                You are an over-doer. <br />

                You may have the tendency of taking <br />
                on too much tasks and responsibilities, <br />
                but struggle to prioritize...
            </div>)
        break
    }
}

  return (
    <>
      <Head>
        <title>Wonderland</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
          {router.query.title &&
            <ResultsTemplate
          
            title={titleValue(router.query.title)}
            mainImageSrc={mainImgSrcValue(router.query.title)}
            characteristics={characteristicsValue(router.query.title)}
            description={descriptionValue(router.query.title)}
            />
          }
    </>
  )
}
